package module1;

public class JMathClass {
    public static void execute() {
        System.out.println(Math.abs(-16)); // 16
        System.out.println(Math.ceil(15.1)); // 16.0
        System.out.println(Math.floor((10.9))); // 10.0
        System.out.println(Math.max(5, 6)); // 6
        System.out.println(Math.min(5, 6)); // 5
        System.out.println(Math.round(15.4)); // 15
        System.out.println(Math.random()); // Rando values from 1 to 10
        System.out.println((int)(Math.random() * 100));
    }
}
